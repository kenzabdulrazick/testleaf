package kenzie;

public class IsPrime {
	public static void main(String args[]) {
		int n=13;
		for(int i=2;i<n;i++) {
			if(n%i==0) {
			
				System.out.println(" is prime"  );
			}
			else { 
				System.out.println("  is not prime" );
			}
			break;

		}
		
	}
}
		
			
		
	


